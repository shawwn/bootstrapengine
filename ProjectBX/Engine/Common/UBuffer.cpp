#include "common_afx.h"

// class header.
#include "UBuffer.h"

//**********************************************************
// class UBuffer
//**********************************************************

//==========================================================
// ctor & dtor
//==========================================================

//----------------------------------------------------------
UBuffer::UBuffer( size_t initSize )
: _ptr( 0 )
, _size( 0 )
{
    ReAlloc( initSize );
}

//----------------------------------------------------------
UBuffer::~UBuffer()
{
    Free();
}


//==========================================================
// public methods
//==========================================================

//----------------------------------------------------------
void
UBuffer::ReAlloc( size_t size )
{
    if ( size > _size )
    {
        delete[] _ptr;
        _ptr = new char[ size ];
        _size = size;
    }
}

//----------------------------------------------------------
void
UBuffer::Free()
{
    delete[] _ptr;
    _ptr = 0;
}
